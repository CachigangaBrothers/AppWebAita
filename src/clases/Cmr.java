package clases;

public class Cmr {
	private int idCmr;
	private int numCmr;
	private int peso;
	
	
	
	public int getIdCmr() {
		return idCmr;
	}
	public void setIdCmr(int idCmr) {
		this.idCmr = idCmr;
	}
	public int getNumCmr() {
		return numCmr;
	}
	public void setNumCmr(int numCmr) {
		this.numCmr = numCmr;
	}
	public int getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}
	
	
}
